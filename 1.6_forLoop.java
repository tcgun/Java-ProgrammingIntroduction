public class Main {

    public static void main(String[] args) {
        //For

        // "i" değişkeni 2'den başlayarak başlar.
        // Döngü, "i" değişkenini 10'dan küçük olduğu sürece çalışır.
        // Her adımda "i" değişkeni 2 artar.
        for(int i=2; i<10; i+=2){
            // "i" değişkeninin değerini ekrana yazdırır.
            System.out.println(i);
        }

        // Döngü tamamlandığında, "For Döngüsü Bitti" mesajını yazdırır.
        System.out.println("For Döngüsü Bitti");
    }
}
